char* custom_str_find(const char* array, const char* str, size_t length) {  
    size_t str_len = 0;  

    // 计算要查找的字符串的长度  
    while (str[str_len] != '\0') {  
        str_len++;  
    }  

    // 遍历数组中的每个字符  
    for (size_t i = 0; i <= length - str_len; i++) {  
        size_t j = 0;  

        // 检查当前位置是否匹配字符串  
        while (j < str_len && array[i + j] == str[j]) {  
            j++;  
        }  

        // 如果找到了指定字符串  
        if (j == str_len) {  
            return (char*)&array[i]; // 返回找到的位置  
        }  
    }  
    
    return NULL; // 没有找到  
}


